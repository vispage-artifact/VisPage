#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"

DEPLOY_DIR="${DEPLOY_DIR:-<VLLM_DEPLOY_DIR>}"
BASE_ENV="${BASE_ENV:-${REPO_ROOT}/configs/sensitivity_8b_locomo_quick/qwen3vl-8b-sens-cache06-gpu1-8021.env}"
CONFIG_TEMPLATE="${CONFIG_TEMPLATE:-${REPO_ROOT}/configs/sensitivity_8b_locomo_quick/cache_util0p6_8021.json}"
OUTPUT_ROOT="${OUTPUT_ROOT:-paper_results/evaluation/sensitivity_8b_locomo_quick/cache_util}"
TMP_DIR="${TMP_DIR:-/tmp/vispage_sensitivity_cache_util_quick}"

UTILS="${UTILS:-0.60}"
CUDA_VISIBLE_DEVICES_OVERRIDE="${CUDA_VISIBLE_DEVICES_OVERRIDE:-1}"
PORT_OVERRIDE="${PORT_OVERRIDE:-8021}"
SESSION_LIMIT="${SESSION_LIMIT:-1}"
PROGRESS_EVERY="${PROGRESS_EVERY:-1}"
SESSION_RETRIES="${SESSION_RETRIES:-1}"
RETRY_SLEEP="${RETRY_SLEEP:-5}"
START_TIMEOUT_SECONDS="${START_TIMEOUT_SECONDS:-900}"
POLL_SECONDS="${POLL_SECONDS:-5}"
PYTHON_BIN="${PYTHON_BIN:-${REPO_ROOT}/.conda-memoryagent/bin/python}"

CURRENT_ENV_FILE=""

cleanup() {
  if [[ -n "${CURRENT_ENV_FILE}" && -f "${CURRENT_ENV_FILE}" ]]; then
    bash "${DEPLOY_DIR}/stop.sh" "${CURRENT_ENV_FILE}" || true
  fi
}
trap cleanup EXIT

wait_for_vllm() {
  local base_url="$1"
  local deadline=$((SECONDS + START_TIMEOUT_SECONDS))

  while (( SECONDS < deadline )); do
    if curl -fsS "${base_url}/v1/models" >/dev/null 2>&1 \
      && curl -fsS -X POST "${base_url}/v1/am/cache/select" \
        -H 'Content-Type: application/json' \
        -d '{"candidates":[]}' >/dev/null 2>&1; then
      return 0
    fi
    sleep "${POLL_SECONDS}"
  done

  return 1
}

write_config_for_util() {
  local util="$1"
  local port="$2"
  local config_path="$3"

  "${PYTHON_BIN}" - "${CONFIG_TEMPLATE}" "${config_path}" "${util}" "${port}" <<'PY'
import json
import sys
from pathlib import Path

template_path = Path(sys.argv[1])
output_path = Path(sys.argv[2])
util = float(sys.argv[3])
port = int(sys.argv[4])
tag = str(sys.argv[3]).replace(".", "p")

cfg = json.loads(template_path.read_text())
cfg["run_name"] = f"sensitivity_8b_locomo_cache_util{tag}"
cfg["runtime"]["base_url"] = f"http://127.0.0.1:{port}"
cfg["renderer"]["output_dir"] = (
    f"paper_results/rendered_pages/sensitivity_8b_locomo_quick/cache_util{tag}"
)
cfg["page"]["key_prefix"] = f"sens8b-cache{tag}-locomo-s4-semantic"
cfg.setdefault("metadata", {})
cfg["metadata"].update({
    "sensitivity": "cache_budget",
    "sensitivity_axis": "gpu_memory_utilization",
    "sensitivity_value": util,
    "server_port": port,
})

output_path.parent.mkdir(parents=True, exist_ok=True)
output_path.write_text(json.dumps(cfg, indent=2) + "\n")
PY
}

mkdir -p "${TMP_DIR}"

if [[ ! -f "${BASE_ENV}" ]]; then
  echo "Missing BASE_ENV=${BASE_ENV}" >&2
  exit 1
fi

if [[ ! -f "${CONFIG_TEMPLATE}" ]]; then
  echo "Missing CONFIG_TEMPLATE=${CONFIG_TEMPLATE}" >&2
  exit 1
fi

if [[ ! -x "${PYTHON_BIN}" ]]; then
  echo "Missing executable PYTHON_BIN=${PYTHON_BIN}" >&2
  exit 1
fi

cd "${REPO_ROOT}"

for util in ${UTILS}; do
  util_tag="${util//./p}"
  base_url="http://127.0.0.1:${PORT_OVERRIDE}"
  run_output_root="${OUTPUT_ROOT}/util_${util_tag}"
  env_file="${TMP_DIR}/qwen3vl-8b-cache-util-${util_tag}-port${PORT_OVERRIDE}.env"
  config_file="${TMP_DIR}/cache_util_${util_tag}_port${PORT_OVERRIDE}.json"
  config_list="${TMP_DIR}/cache_util_${util_tag}_port${PORT_OVERRIDE}.list"
  service_name="vllm-qwen3vl-8b-cache-util-${util_tag}-port${PORT_OVERRIDE}"

  cp "${BASE_ENV}" "${env_file}"
  {
    echo
    echo "# Auto-generated by run_sensitivity_cache_util_quick.sh"
    echo "SERVICE_NAME=${service_name}"
    echo "CUDA_VISIBLE_DEVICES=${CUDA_VISIBLE_DEVICES_OVERRIDE}"
    echo "PORT=${PORT_OVERRIDE}"
    echo "GPU_MEMORY_UTILIZATION=${util}"
    echo "VLLM_NO_USAGE_STATS=1"
  } >> "${env_file}"

  write_config_for_util "${util}" "${PORT_OVERRIDE}" "${config_file}"
  printf '%s\n' "${config_file}" > "${config_list}"

  CURRENT_ENV_FILE="${env_file}"

  echo "=== cache util=${util} gpu=${CUDA_VISIBLE_DEVICES_OVERRIDE} port=${PORT_OVERRIDE} output=${run_output_root} ==="
  bash "${DEPLOY_DIR}/stop.sh" "${env_file}" || true
  bash "${DEPLOY_DIR}/start.sh" "${env_file}"

  if ! wait_for_vllm "${base_url}"; then
    echo "vLLM did not become ready for util=${util}; stopping and continuing" >&2
    bash "${DEPLOY_DIR}/stop.sh" "${env_file}" || true
    continue
  fi

  args=(
    "${PYTHON_BIN}" evaluation/autoeval/run_sessions.py
    --config-list "${config_list}"
    --output-root "${run_output_root}"
    --schedule config
    --progress-every "${PROGRESS_EVERY}"
    --session-retries "${SESSION_RETRIES}"
    --retry-sleep "${RETRY_SLEEP}"
  )

  if [[ -n "${SESSION_LIMIT}" && "${SESSION_LIMIT}" != "0" ]]; then
    args+=(--session-limit "${SESSION_LIMIT}")
  fi

  set +e
  PYTHONPATH=src "${args[@]}"
  run_status=$?
  set -e

  bash "${DEPLOY_DIR}/stop.sh" "${env_file}" || true

  if [[ "${run_status}" -ne 0 ]]; then
    echo "autoeval failed for util=${util} with status=${run_status}; continuing" >&2
  fi
done

CURRENT_ENV_FILE=""
echo "cache util quick sensitivity finished: ${OUTPUT_ROOT}"
